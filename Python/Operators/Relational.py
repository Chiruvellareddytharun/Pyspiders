#Relational is also known as comparision
#six relational operators ==,<,>,<=,>=,!=

#example
a,b,c = 10,20,10
print(a==b)    #false
print(a<=b)    #true
print(a<b)     #true
print(b>c)     #true
print(b>=a)    #true
print(a!=c)    #false  
  

